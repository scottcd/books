# You can use '' or "" for strings
string1 = "This is a string."
string2 = 'This is also a string.'

print(string1)
print(string2)

# This will allow for quotes and
# apostrophes to be flexibly used
quotes = 'I told my friend, "Python is my favorite language!"'
apostrophe1 = "The language 'Python' is named after Monty Python, not the snake."
apostrophe2 = "One of Python's strengths is its diverse and supportive community"

print(quotes)
print(apostrophe1)
print(apostrophe2)

# adding tabs or newlines to strings is easy.
print('Python')   
print('\tPython') # tab     \t
print('\nPython') # newline \n