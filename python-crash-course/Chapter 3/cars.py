cars = ['bmw', 'audi', 'toyota', 'subaru']

# sort by alphabetical order; sort is permanent
cars.sort()
print(cars)

# sort by reverse alphabetical order; sort is permanent
cars.sort(reverse=True)
print(cars)

# to get a temporarily sorted list, use sorted()
print(sorted(cars))

cars = ['bmw', 'audi', 'toyota', 'subaru']

# reverse the list, without sorting
cars.reverse()
print(cars)

# get the length of a list
length = len(cars)
print(length)

